/**
 * TRY IT YOURSELF — curated Homebrew composer (static site, no API).
 */
(function () {
  'use strict';

  var TAP_LINE = 'brew tap MzxzD/janet';

  var BASE = {
    mesh: {
      pkgs: ['MzxzD/janet/janet-mesh'],
      hints: [
        '# After install — mesh + Tailscale:',
        'brew services start tailscale   # Linux (Homebrew); macOS: use Tailscale.app or CLI',
        'sudo tailscale up                 # Linux; macOS may use GUI login',
        'janet-mesh-env',
        'export JANET_API_HOST=0.0.0.0',
        'janet-api-server                  # :8080 — also see Remote Janet guide for remote clients',
      ],
    },
    metajanet: {
      pkgs: ['MzxzD/janet/janet'],
      hints: [
        '# Full Janet meta formula (pulls janet-seed, menu bar, recommended deps):',
        'export JANET_API_HOST=0.0.0.0   # if clients reach you over LAN / Tailscale',
        'janet-api-server',
      ],
    },
    seed: {
      pkgs: ['MzxzD/janet/janet-seed'],
      hints: [
        '# Core janet-seed only:',
        'export JANET_API_HOST=0.0.0.0',
        'janet-api-server',
      ],
    },
    janetall: {
      pkgs: ['MzxzD/janet/janet-all'],
      hints: [
        '# janet-all — API + menu bar from janet-seed archive; see `brew info janet-all`',
        'export JANET_API_HOST=0.0.0.0',
      ],
    },
  };

  var ADDON = {
    awakening: {
      pkgs: ['MzxzD/janet/janet-awakening'],
      hints: ['# Janet Awakening: IDE integration — Continue.dev friendly defaults'],
    },
    toolchain: {
      pkgs: ['MzxzD/janet/janet-arm64-toolchain'],
      hints: ['# Optional Apple Silicon toolchain helpers'],
    },
  };

  function getBaseCard() {
    return document.querySelector('[data-tier="base"][aria-checked="true"]');
  }

  function getAddonCards() {
    return document.querySelectorAll('[data-tier="addon"][aria-checked="true"]');
  }

  function composeScript() {
    var baseKey = getBaseCard() ? getBaseCard().getAttribute('data-feature') : null;
    var lines = [TAP_LINE, ''];
    var pkgs = {};
    var hintBlocks = [];

    if (baseKey && BASE[baseKey]) {
      BASE[baseKey].pkgs.forEach(function (p) {
        pkgs[p] = true;
      });
      hintBlocks.push.apply(hintBlocks, BASE[baseKey].hints);
    }

    getAddonCards().forEach(function (btn) {
      var k = btn.getAttribute('data-feature');
      if (!ADDON[k]) return;
      ADDON[k].pkgs.forEach(function (p) {
        pkgs[p] = true;
      });
      hintBlocks.push.apply(hintBlocks, ADDON[k].hints);
    });

    var pkgList = Object.keys(pkgs).sort();
    if (pkgList.length === 0) {
      lines.push('# Pick one base stack (or add optional extras).');
      lines.push('# Commands are curated on this page — not generated by AI.');
      return lines.join('\n');
    }

    lines.push('brew install ' + pkgList.join(' '));
    lines.push('');
    lines.push.apply(lines, hintBlocks);
    lines.push('');
    lines.push('# Docs: https://heyjanet.org/remote-access-tailscale.html');
    return lines.join('\n');
  }

  function refreshOutput() {
    var pre = document.getElementById('composer-output');
    if (pre) pre.textContent = composeScript();
  }

  function setBase(feature) {
    document.querySelectorAll('[data-tier="base"]').forEach(function (btn) {
      var on = btn.getAttribute('data-feature') === feature;
      btn.setAttribute('aria-checked', on ? 'true' : 'false');
    });
    refreshOutput();
  }

  function toggleAddon(feature) {
    var btn = document.querySelector('[data-tier="addon"][data-feature="' + feature + '"]');
    if (!btn) return;
    var on = btn.getAttribute('aria-checked') === 'true';
    btn.setAttribute('aria-checked', on ? 'false' : 'true');
    refreshOutput();
  }

  function initCards() {
    document.querySelectorAll('.wish-card').forEach(function (card) {
      card.addEventListener('click', function () {
        var tier = card.getAttribute('data-tier');
        var feature = card.getAttribute('data-feature');
        if (tier === 'base') setBase(feature);
        else toggleAddon(feature);
      });
      card.addEventListener('keydown', function (e) {
        if (e.key === 'Enter' || e.key === ' ') {
          e.preventDefault();
          card.click();
        }
      });
    });
  }

  function initCopy() {
    var btn = document.getElementById('composer-copy');
    var pre = document.getElementById('composer-output');
    if (!btn || !pre) return;
    btn.addEventListener('click', function () {
      var text = pre.textContent;
      navigator.clipboard.writeText(text).then(function () {
        btn.textContent = 'copied';
        btn.classList.add('copied');
        setTimeout(function () {
          btn.textContent = 'copy';
          btn.classList.remove('copied');
        }, 1800);
      });
    });
  }

  document.addEventListener('DOMContentLoaded', function () {
    initCards();
    initCopy();
    setBase('metajanet');
  });
})();
